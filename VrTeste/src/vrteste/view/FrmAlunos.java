    private void jTableAlunosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableAlunosMouseClicked
        // TODO add your handling code here:
        int index = jTableAlunos.getSelectedRow();

        codAluno = Integer.valueOf(jTableAlunos.getValueAt(index, 0).toString());

        jTextFieldMatricula.setText(jTableAlunos.getValueAt(index, 1).toString());
        jTextFieldNome.setText(jTableAlunos.getValueAt(index, 2).toString());
        jTextFieldRg.setText(jTableAlunos.getValueAt(index, 3).toString());
        jTextFieldCpf.setText(jTableAlunos.getValueAt(index, 4).toString());
    }//GEN-LAST:event_jTableAlunosMouseClicked

    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        // TODO add your handling code here:
        int index = -1;

        index = jTableAlunos.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um registro antes!");
        } else {
            if (alunoControl.deletar(jTableAlunos.getValueAt(index, 1).toString(), jTableAlunos.getValueAt(index, 2).toString(), jTableAlunos.getValueAt(index, 3).toString(), jTableAlunos.getValueAt(index, 4).toString())) {
                JOptionPane.showMessageDialog(this, "Registro Deletado com sucesso!");
                populaTabela();
            }
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed


