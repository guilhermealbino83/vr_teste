    private void limpaCampos() {
        jTextFieldMatricula.setText("");
        jTextFieldNome.setText("");
        jTextFieldRg.setText("");
        jTextFieldCpf.setText("");
        codAluno = -1;
    }

    private void populaTabela() {
        alunoTabModelo = new DefaultTableModel();

        Map<Integer, String[]> listaAlunos = new HashMap<>();

        alunoTabModelo.addColumn("Codigo");
        alunoTabModelo.addColumn("Matricula");
        alunoTabModelo.addColumn("Nome");
        alunoTabModelo.addColumn("RG");
        alunoTabModelo.addColumn("CPF");

        listaAlunos = alunoControl.listarTodos();

        for (int codAluno : listaAlunos.keySet()) {
            String[] elemento = listaAlunos.get(codAluno);
            alunoTabModelo.addRow(new Object[]{codAluno, elemento[0], elemento[1], elemento[2], elemento[3]});
        }
        jTableAlunos.setModel(alunoTabModelo);
    }

    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        if (indNovo) {
            if (alunoControl.salvar(jTextFieldMatricula.getText(), jTextFieldNome.getText(), jTextFieldRg.getText(), jTextFieldCpf.getText())) {
                JOptionPane.showMessageDialog(this, "Cadastro Salvo com Sucesso");

            }
        } else {
            int index = -1;
            index = jTableAlunos.getSelectedRow();
            if (index == -1) {
                JOptionPane.showMessageDialog(this, "Selecione um registro antes!");
            } else {
                if (alunoControl.alterar(codAluno, jTextFieldMatricula.getText(), jTextFieldNome.getText(), jTextFieldRg.getText(), jTextFieldCpf.getText())) {
                    JOptionPane.showMessageDialog(this, "Cadastro Alterado com Sucesso");
                }
            }
        }
        habilitaComponentes(false, true, true, true, false, false, false, false, false);
        limpaCampos();
        populaTabela();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

